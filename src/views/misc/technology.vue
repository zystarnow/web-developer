    <h5>方案二：Node + Vue + webPack 前后端分离开发模式</h5>
    <p><strong>优点：</strong></p>
    <p>JS可以做很大部分的数据处理工作，无需渲染页面，java只负责使用rest收发json数据，对服务器的压力减小到最小，性能肯定更高了。</p>
    <p>后台错误不会直接反映到前台，错误接秒较为友好。</p>
    <p>手机App与PC端网站可以共用同一套接口。</p>
    <p><strong>缺点：</strong>单页应用相对于传统的网页来说SEO并不是那么友好，书签功能需要自己写代码支持。</p>
